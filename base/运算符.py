# 算数运算符

# -  取反
a = 16
print(-a)
# +
b = 12
print(a + b)
# -
print(a - b)
# *
print(a * b)
# /
print(a/b)
# ** 幂
print(a**b)
# // 除取整
print(a//b)


# 关系运算符  除了可以用来比较int外 数组 元组之类的也可以进行比较
print(a == b)
print(a != b)
print(a < b)
print(a > b)
print(a >= b)
print(a <= b)



# 逻辑运算符  与Java一致 也可能造成短路现象
print(True and False)
print(True and True)
print(True or False)
print(True or True)
print(not True)


# 位运算
c = 0b10110010
d = 0b01011110
print(~c)
print(c | d)
print(c & d)
print(c ^ d)
print(c >> 2)
print(c << 2)



